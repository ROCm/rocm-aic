"""
vLLM bench serve load generator.
"""

import json
import re
import subprocess
from pathlib import Path
from typing import Dict, Any, List

from .base import LoadGeneratorBase


class VllmBenchServe(LoadGeneratorBase):
    """vLLM bench serve benchmarking tool."""

    def build_args(self, benchmark_args: Dict[str, Any], model: str,
                   service_info: Dict[str, str], **kwargs) -> List[str]:
        """
        Build command-line arguments for vllm-bench-serve tool.

        Args:
            benchmark_args: Additional benchmark configuration
            model: Model name
            service_info: Dict containing 'base_url' key
            **kwargs: Must contain 'run_dir' and 'seed'

        Returns:
            List of command-line arguments
        """
        run_dir = kwargs['run_dir']
        seed = kwargs['seed']

        args = [
            "--base-url", service_info['base_url'],
            "--model", model,
        ]

        # Add seed if not already in benchmark_args
        if 'seed' not in benchmark_args:
            args.extend(["--seed", str(seed)])

        # Always save results as JSON to a directory that exists in the pod
        # The runner script will extract the JSON file from this location
        args.append("--save-result")
        args.extend(["--result-dir", "/tmp/results"])

        # Add additional benchmark arguments
        args.extend(self.orchestrator._convert_args_dict(benchmark_args))

        return args

    def parse_metrics(self, output_file: Path) -> Dict[str, Any]:
        """
        Parse vLLM bench serve output (JSON or text format).

        Extracts performance metrics from either JSON or text output files
        generated by vllm bench serve. Prefers JSON format over text.

        Args:
            output_file: Path to benchmark_output_runN.json or benchmark_output_runN.log

        Returns:
            Dictionary with:
                - metrics: Nested dict containing parsed metrics
                - parsing_status: "success" or "failed"
                - parsing_errors: List of errors (if any)
        """
        # If given a .log file, check if a .json version exists and prefer it
        if output_file.suffix == '.log':
            json_file = output_file.with_suffix('.json')
            if json_file.exists():
                return self._parse_json_output(json_file)
            return self._parse_text_output(output_file)
        else:
            # .json file provided or other extension
            return self._parse_json_output(output_file)

    def _parse_json_output(self, output_file: Path) -> Dict[str, Any]:
        """Parse vLLM bench serve JSON output format."""
        result = {
            "metrics": {},
            "parsing_status": "failed",
            "parsing_errors": []
        }

        try:
            # Check if file exists
            if not output_file.exists():
                result["parsing_errors"].append(f"Output file not found: {output_file}")
                return result

            # Load JSON
            with open(output_file, 'r') as f:
                data = json.load(f)

            # Extract core metrics
            metrics = {}

            # Request completion stats
            for key in ["completed_requests", "total_requests",
                       "request_throughput", "output_throughput"]:
                if key in data:
                    metrics[key] = data[key]

            # TTFT metrics (Time To First Token)
            ttft = {}
            for stat in ["mean", "median", "p99", "p95", "p90", "p75", "p50", "p25"]:
                key = f"{stat}_ttft_ms"
                if key in data:
                    ttft[f"{stat}_ms"] = data[key]
            if ttft:
                metrics["ttft"] = ttft

            # TPOT metrics (Time Per Output Token)
            tpot = {}
            for stat in ["mean", "median", "p99", "p95", "p90", "p75", "p50", "p25"]:
                key = f"{stat}_tpot_ms"
                if key in data:
                    tpot[f"{stat}_ms"] = data[key]
            if tpot:
                metrics["tpot"] = tpot

            # ITL metrics (Inter-Token Latency)
            itl = {}
            for stat in ["mean", "median", "p99", "p95", "p90", "p75", "p50", "p25"]:
                key = f"{stat}_itl_ms"
                if key in data:
                    itl[f"{stat}_ms"] = data[key]
            if itl:
                metrics["itl"] = itl

            # E2E latency metrics (if available)
            e2e = {}
            for stat in ["mean", "median", "p99", "p95", "p90"]:
                key = f"{stat}_e2e_latency_ms"
                if key in data:
                    e2e[f"{stat}_ms"] = data[key]
            if e2e:
                metrics["e2e_latency"] = e2e

            result["metrics"] = metrics
            result["parsing_status"] = "success"

        except json.JSONDecodeError as e:
            result["parsing_errors"].append(f"Invalid JSON: {str(e)}")
        except Exception as e:
            result["parsing_errors"].append(f"Unexpected error: {str(e)}")

        return result

    def _parse_text_output(self, output_file: Path) -> Dict[str, Any]:
        """
        Parse vLLM bench serve text output format.

        Parses the text-based output from vLLM bench serve when --save-result is not used
        or when the output is saved to .log files.
        """
        result = {
            "metrics": {},
            "parsing_status": "failed",
            "parsing_errors": []
        }

        try:
            # Check if file exists
            if not output_file.exists():
                result["parsing_errors"].append(f"Output file not found: {output_file}")
                return result

            # Read file content
            with open(output_file, 'r') as f:
                content = f.read()

            metrics = {}
            found_any = False

            # Parse the benchmark result section
            # Look for pattern: "Successful requests:                     30"
            match = re.search(r'Successful requests:\s+(\d+)', content)
            if match:
                metrics["completed_requests"] = int(match.group(1))
                found_any = True

            match = re.search(r'Failed requests:\s+(\d+)', content)
            if match:
                failed = int(match.group(1))
                metrics["failed_requests"] = failed
                if "completed_requests" in metrics:
                    metrics["total_requests"] = metrics["completed_requests"] + failed

            # Maximum request concurrency: "Maximum request concurrency:             8"
            match = re.search(r'Maximum request concurrency:\s+([\d.]+)', content)
            if match:
                metrics["max_request_concurrency"] = float(match.group(1))
                found_any = True

            # Benchmark duration: "Benchmark duration (s):                  204.97"
            match = re.search(r'Benchmark duration \(s\):\s+([\d.]+)', content)
            if match:
                metrics["benchmark_duration_s"] = float(match.group(1))
                found_any = True

            # Total input tokens: "Total input tokens:                      640000"
            match = re.search(r'Total input tokens:\s+(\d+)', content)
            if match:
                metrics["total_input_tokens"] = int(match.group(1))
                found_any = True

            # Total generated tokens: "Total generated tokens:                  80000"
            match = re.search(r'Total generated tokens:\s+(\d+)', content)
            if match:
                metrics["total_generated_tokens"] = int(match.group(1))
                found_any = True

            # Request throughput: "Request throughput (req/s):              9.50"
            match = re.search(r'Request throughput \(req/s\):\s+([\d.]+)', content)
            if match:
                metrics["request_throughput"] = float(match.group(1))
                found_any = True

            # Output token throughput: "Output token throughput (tok/s):         9.50"
            match = re.search(r'Output token throughput \(tok/s\):\s+([\d.]+)', content)
            if match:
                metrics["output_throughput"] = float(match.group(1))
                found_any = True

            # Peak output token throughput: "Peak output token throughput (tok/s):    480.00"
            match = re.search(r'Peak output token throughput \(tok/s\):\s+([\d.]+)', content)
            if match:
                metrics["peak_output_throughput"] = float(match.group(1))
                found_any = True

            # Peak concurrent requests: "Peak concurrent requests:                16.00"
            match = re.search(r'Peak concurrent requests:\s+([\d.]+)', content)
            if match:
                metrics["peak_concurrent_requests"] = float(match.group(1))
                found_any = True

            # Total token throughput: "Total token throughput (tok/s):          9506.84"
            match = re.search(r'Total token throughput \(tok/s\):\s+([\d.]+)', content)
            if match:
                metrics["total_token_throughput"] = float(match.group(1))

            # TTFT metrics
            ttft = {}
            match = re.search(r'Mean TTFT \(ms\):\s+([\d.]+)', content)
            if match:
                ttft["mean_ms"] = float(match.group(1))
                found_any = True

            match = re.search(r'Median TTFT \(ms\):\s+([\d.]+)', content)
            if match:
                ttft["median_ms"] = float(match.group(1))

            match = re.search(r'P99 TTFT \(ms\):\s+([\d.]+)', content)
            if match:
                ttft["p99_ms"] = float(match.group(1))

            if ttft:
                metrics["ttft"] = ttft

            # TPOT metrics
            tpot = {}
            match = re.search(r'Mean TPOT \(ms\):\s+([\d.]+)', content)
            if match:
                tpot["mean_ms"] = float(match.group(1))

            match = re.search(r'Median TPOT \(ms\):\s+([\d.]+)', content)
            if match:
                tpot["median_ms"] = float(match.group(1))

            match = re.search(r'P99 TPOT \(ms\):\s+([\d.]+)', content)
            if match:
                tpot["p99_ms"] = float(match.group(1))

            if tpot:
                metrics["tpot"] = tpot

            # ITL metrics
            itl = {}
            match = re.search(r'Mean ITL \(ms\):\s+([\d.]+)', content)
            if match:
                itl["mean_ms"] = float(match.group(1))

            match = re.search(r'Median ITL \(ms\):\s+([\d.]+)', content)
            if match:
                itl["median_ms"] = float(match.group(1))

            match = re.search(r'P99 ITL \(ms\):\s+([\d.]+)', content)
            if match:
                itl["p99_ms"] = float(match.group(1))

            if itl:
                metrics["itl"] = itl

            result["metrics"] = metrics

            if found_any:
                result["parsing_status"] = "success"
            else:
                result["parsing_status"] = "failed"
                result["parsing_errors"].append("No recognized metrics found in output")

        except Exception as e:
            result["parsing_errors"].append(f"Unexpected error: {str(e)}")

        return result

    def run(self, config: Dict[str, Any], run_dir: Path,
            params: Dict[str, Any], namespace: str) -> Dict[str, Any]:
        """Run vLLM bench serve benchmark."""

        # Get configuration
        image = config.get('image', 'vllm/vllm-openai:latest')
        clone_run = config.get('clone_run', False)

        # Get benchmark_args - handle both patterns
        benchmark_args_config = config.get('benchmark_args', {})
        if isinstance(benchmark_args_config, dict) and benchmark_args_config.get('type') in ['combinations', 'pairwise']:
            # New pattern: type: combinations or pairwise - extract fixed args (non-sweepable)
            benchmark_args = {}
            for key, value in benchmark_args_config.get('args', {}).items():
                # Skip sweepable parameters (they're in _load_params)
                if not (isinstance(value, dict) and 'values' in value):
                    benchmark_args[key] = value
        else:
            # Old pattern: direct dict or sweep_args pattern
            benchmark_args = benchmark_args_config.copy() if benchmark_args_config else {}

        # Merge load generation sweep parameters (if any)
        if '_load_params' in params:
            benchmark_args.update(params['_load_params'])

        # Get model and construct base URL
        model = params.get('model')
        base_url = f"http://llm-d-inference-gateway-istio.{namespace}.svc.cluster.local:80"

        # Generate or get seed
        if 'seed' in benchmark_args:
            seed = benchmark_args['seed']
        else:
            seed = self.orchestrator.generate_timestamp_seed()
            print(f"  Generated seed: {seed}")

        # Determine number of runs
        num_runs = 2 if clone_run else 1

        results = []
        for run_idx in range(1, num_runs + 1):
            run_label = f"run{run_idx}"
            print(f"  Executing benchmark {run_label}/{num_runs}...")

            result = self._execute_single_run(
                benchmark_args=benchmark_args,
                model=model,
                base_url=base_url,
                run_dir=run_dir,
                run_label=run_label,
                seed=seed,
                image=image,
                namespace=namespace
            )
            results.append(result)

        # Return aggregated results
        return {
            "tool": "vllm-bench-serve",
            "runs": results,
            "clone_run": clone_run,
            "seed": seed,
            "num_runs": num_runs,
            "exit_code": 0 if all(r['exit_code'] == 0 for r in results) else 1
        }

    def _execute_single_run(self, benchmark_args: Dict[str, Any], model: str,
                            base_url: str, run_dir: Path, run_label: str,
                            seed: int, image: str, namespace: str) -> Dict[str, Any]:
        """Execute a single vLLM bench serve benchmark run."""

        # Build benchmark arguments
        benchmark_cmd_args = self.build_args(
            benchmark_args,
            model,
            {'base_url': base_url},
            run_dir=run_dir,
            seed=seed
        )

        # Build command using generic runner
        cmd = [
            "./scripts/run-k8s-benchmark.sh",
            "--tool-name", "vllm-bench-serve",
            "--manifest-generator", "./load-generators/vllm-bench-serve/generate-pod-manifest.sh",
            "--image", image,
            "--namespace", namespace,
            "--output-dir", str(run_dir.absolute()),
            "--run-label", run_label,
            "--completion-timeout", "10800",
            "--",  # Separator for benchmark args
        ]
        cmd.extend(benchmark_cmd_args)

        print(f"    Running vllm bench serve (image: {image})...")

        # Execute
        result = subprocess.run(cmd, capture_output=True, text=True)

        if result.returncode != 0:
            print(f"    Warning: Benchmark exited with code {result.returncode}")

        # Save runner output
        output_file = run_dir / f"benchmark_runner_output_{run_label}.txt"
        with open(output_file, "w") as f:
            f.write(result.stdout)
            f.write(result.stderr)

        # Parse metrics from output file
        output_json_path = run_dir / f"benchmark_output_{run_label}.json"
        parsed_result = self.parse_metrics(output_json_path)

        return {
            "run_label": run_label,
            "output_file": str(output_json_path),
            "exit_code": result.returncode,
            "seed": seed,
            **parsed_result  # Add metrics, parsing_status, parsing_errors
        }
