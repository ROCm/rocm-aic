"""
LMCache bench load generator.
"""

import re
import subprocess
from pathlib import Path
from typing import Dict, Any, List

from .base import LoadGeneratorBase


class LMCacheBenchLoadGenerator(LoadGeneratorBase):
    """LMCache bench benchmarking tool."""

    def build_args(self, benchmark_args: Dict[str, Any], model: str,
                   service_info: Dict[str, str], **kwargs) -> List[str]:
        """
        Build command-line arguments for lmcache bench engine tool.

        Args:
            benchmark_args: Additional benchmark configuration (not used, config comes from ConfigMap)
            model: Model name (not used, comes from bench_config.json)
            service_info: Dict containing 'engine_url' key
            **kwargs: Must contain 'run_dir' (not used by lmcache bench)

        Returns:
            List of command-line arguments
        """
        args = [
            "--engine-url", service_info['engine_url'],
            "--config", "/opt/bench-configs/bench_config.json"
        ]

        return args

    def parse_metrics(self, output_file: Path) -> Dict[str, Any]:
        """
        Parse lmcache bench output (text format).

        Extracts performance metrics from the text output files
        generated by lmcache bench engine.

        Args:
            output_file: Path to benchmark_output_runN.log

        Returns:
            Dictionary with:
                - metrics: Nested dict containing parsed metrics
                - parsing_status: "success" or "failed"
                - parsing_errors: List of errors (if any)
        """
        result = {
            "metrics": {},
            "parsing_status": "failed",
            "parsing_errors": []
        }

        try:
            # Check if file exists
            if not output_file.exists():
                result["parsing_errors"].append(f"Output file not found: {output_file}")
                return result

            # Read file content
            with open(output_file, 'r') as f:
                content = f.read()

            metrics = {}
            found_any = False

            # Parse benchmark results
            # Note: The exact output format of lmcache bench is not documented,
            # so these patterns are based on common benchmark output formats.
            # Adjust as needed based on actual lmcache bench output.

            # Total requests
            match = re.search(r'(?:Total|Completed) requests?:\s*(\d+)', content, re.IGNORECASE)
            if match:
                metrics["total_requests"] = int(match.group(1))
                found_any = True

            # Failed requests
            match = re.search(r'Failed requests?:\s*(\d+)', content, re.IGNORECASE)
            if match:
                metrics["failed_requests"] = int(match.group(1))
                found_any = True

            # Benchmark duration
            match = re.search(r'(?:Benchmark )?[Dd]uration\s*(?:\(s\))?\s*[:=]\s*([\d.]+)', content)
            if match:
                metrics["duration_s"] = float(match.group(1))
                found_any = True

            # Throughput metrics
            match = re.search(r'(?:Request )?[Tt]hroughput\s*(?:\(req/s\))?\s*[:=]\s*([\d.]+)', content)
            if match:
                metrics["request_throughput"] = float(match.group(1))
                found_any = True

            match = re.search(r'Token throughput\s*(?:\(tok/s\))?\s*[:=]\s*([\d.]+)', content)
            if match:
                metrics["token_throughput"] = float(match.group(1))
                found_any = True

            # Latency metrics (TTFT - Time To First Token)
            ttft = {}
            match = re.search(r'Mean TTFT\s*(?:\(ms\))?\s*[:=]\s*([\d.]+)', content, re.IGNORECASE)
            if match:
                ttft["mean_ms"] = float(match.group(1))
                found_any = True

            match = re.search(r'Median TTFT\s*(?:\(ms\))?\s*[:=]\s*([\d.]+)', content, re.IGNORECASE)
            if match:
                ttft["median_ms"] = float(match.group(1))

            match = re.search(r'P99 TTFT\s*(?:\(ms\))?\s*[:=]\s*([\d.]+)', content, re.IGNORECASE)
            if match:
                ttft["p99_ms"] = float(match.group(1))

            match = re.search(r'P95 TTFT\s*(?:\(ms\))?\s*[:=]\s*([\d.]+)', content, re.IGNORECASE)
            if match:
                ttft["p95_ms"] = float(match.group(1))

            if ttft:
                metrics["ttft"] = ttft

            # TPOT (Time Per Output Token)
            tpot = {}
            match = re.search(r'Mean TPOT\s*(?:\(ms\))?\s*[:=]\s*([\d.]+)', content, re.IGNORECASE)
            if match:
                tpot["mean_ms"] = float(match.group(1))
                found_any = True

            match = re.search(r'Median TPOT\s*(?:\(ms\))?\s*[:=]\s*([\d.]+)', content, re.IGNORECASE)
            if match:
                tpot["median_ms"] = float(match.group(1))

            match = re.search(r'P99 TPOT\s*(?:\(ms\))?\s*[:=]\s*([\d.]+)', content, re.IGNORECASE)
            if match:
                tpot["p99_ms"] = float(match.group(1))

            if tpot:
                metrics["tpot"] = tpot

            # Cache hit rate (specific to lmcache bench)
            match = re.search(r'Cache hit rate\s*[:=]\s*([\d.]+)%?', content, re.IGNORECASE)
            if match:
                metrics["cache_hit_rate"] = float(match.group(1))
                found_any = True

            # Cache miss rate
            match = re.search(r'Cache miss rate\s*[:=]\s*([\d.]+)%?', content, re.IGNORECASE)
            if match:
                metrics["cache_miss_rate"] = float(match.group(1))
                found_any = True

            # Decode cache hits
            match = re.search(r'Decode cache hits?\s*[:=]\s*(\d+)', content, re.IGNORECASE)
            if match:
                metrics["decode_cache_hits"] = int(match.group(1))
                found_any = True

            # Prefill cache hits
            match = re.search(r'Prefill cache hits?\s*[:=]\s*(\d+)', content, re.IGNORECASE)
            if match:
                metrics["prefill_cache_hits"] = int(match.group(1))
                found_any = True

            result["metrics"] = metrics

            if found_any:
                result["parsing_status"] = "success"
            else:
                result["parsing_status"] = "failed"
                result["parsing_errors"].append("No recognized metrics found in output")

        except Exception as e:
            result["parsing_errors"].append(f"Unexpected error: {str(e)}")

        return result

    def run(self, config: Dict[str, Any], run_dir: Path,
            params: Dict[str, Any], namespace: str) -> Dict[str, Any]:
        """Run lmcache bench benchmark."""

        # Get configuration
        image = config.get('image', 'lmcache/lmcache:latest')
        clone_run = config.get('clone_run', False)

        # Construct engine URL - use gateway service like vllm_bench_serve
        engine_url = f"http://llm-d-inference-gateway-istio.{namespace}.svc.cluster.local:80"

        # Determine number of runs
        num_runs = 2 if clone_run else 1

        results = []
        for run_idx in range(1, num_runs + 1):
            run_label = f"run{run_idx}"
            print(f"  Executing benchmark {run_label}/{num_runs}...")

            result = self._execute_single_run(
                engine_url=engine_url,
                run_dir=run_dir,
                run_label=run_label,
                image=image,
                namespace=namespace
            )
            results.append(result)

        # Return aggregated results
        return {
            "tool": "lmcache-bench",
            "runs": results,
            "clone_run": clone_run,
            "num_runs": num_runs,
            "exit_code": 0 if all(r['exit_code'] == 0 for r in results) else 1
        }

    def _execute_single_run(self, engine_url: str, run_dir: Path,
                            run_label: str, image: str, namespace: str) -> Dict[str, Any]:
        """Execute a single lmcache bench benchmark run."""

        # Build benchmark arguments
        benchmark_cmd_args = self.build_args(
            {},  # benchmark_args not used for lmcache bench
            "",  # model comes from config file
            {'engine_url': engine_url},
            run_dir=run_dir
        )

        # Build command using generic runner
        cmd = [
            "./scripts/run-k8s-benchmark.sh",
            "--tool-name", "lmcache-bench",
            "--manifest-generator", "./load-generators/lmcache-bench/generate-pod-manifest.sh",
            "--image", image,
            "--namespace", namespace,
            "--output-dir", str(run_dir.absolute()),
            "--run-label", run_label,
            "--completion-timeout", "1200",
            "--",  # Separator for benchmark args
        ]
        cmd.extend(benchmark_cmd_args)

        print(f"    Running lmcache bench (image: {image})...")

        # Execute
        result = subprocess.run(cmd, capture_output=True, text=True)

        if result.returncode != 0:
            print(f"    Warning: Benchmark exited with code {result.returncode}")

        # Save runner output
        output_file = run_dir / f"benchmark_runner_output_{run_label}.txt"
        with open(output_file, "w") as f:
            f.write(result.stdout)
            f.write(result.stderr)

        # Parse metrics from output file
        output_log_path = run_dir / f"benchmark_output_{run_label}.log"
        parsed_result = self.parse_metrics(output_log_path)

        return {
            "run_label": run_label,
            "output_file": str(output_log_path),
            "exit_code": result.returncode,
            **parsed_result  # Add metrics, parsing_status, parsing_errors
        }
