"""
Multi-turn benchmark load generator.
"""

import re
import subprocess
from pathlib import Path
from typing import Dict, Any, List

from .base import LoadGeneratorBase


class MultiTurnBenchmark(LoadGeneratorBase):
    """Multi-turn conversation benchmark tool."""

    def build_args(self, benchmark_args: Dict[str, Any], model: str,
                   service_info: Dict[str, str], **kwargs) -> List[str]:
        """
        Build command-line arguments for multi-turn-benchmark tool.

        Args:
            benchmark_args: Additional benchmark configuration
            model: Model name
            service_info: Dict containing 'url' key with service URL
            **kwargs: Unused

        Returns:
            List of command-line arguments
        """
        args = [
            "--model", model,
            "--url", service_info['url']
        ]

        # Add additional benchmark arguments
        args.extend(self.orchestrator._convert_args_dict(benchmark_args))

        return args

    def parse_metrics(self, output_file: Path) -> Dict[str, Any]:
        """
        Parse multi-turn benchmark text output.

        Extracts performance metrics from the text log file generated by
        the multi-turn benchmark tool using regex patterns.

        Args:
            output_file: Path to the benchmark_output.txt file

        Returns:
            Dictionary with:
                - metrics: Dict containing parsed metrics
                - parsing_status: "success", "partial", or "failed"
                - parsing_errors: List of errors (if any)
                - parsing_warnings: List of warnings (if any)
        """
        result = {
            "metrics": {},
            "parsing_status": "failed",
            "parsing_errors": [],
            "parsing_warnings": []
        }

        try:
            # Check if file exists
            if not output_file.exists():
                result["parsing_errors"].append(f"Output file not found: {output_file}")
                return result

            # Read file content
            with open(output_file, 'r') as f:
                content = f.read()

            metrics = {}
            found_any = False

            # Pattern: "Completed: 95/100 successful" or similar
            match = re.search(r'Completed:\s*(\d+)/(\d+)\s+successful', content, re.IGNORECASE)
            if match:
                successful = int(match.group(1))
                total = int(match.group(2))
                metrics["successful_conversations"] = successful
                metrics["total_conversations"] = total
                metrics["failed_conversations"] = total - successful
                metrics["success_rate"] = successful / total if total > 0 else 0.0
                found_any = True

            # Pattern: "Mean TTFT: 125ms" or "Mean TTFT: 125.4 ms"
            match = re.search(r'Mean\s+TTFT:\s*([\d.]+)\s*ms', content, re.IGNORECASE)
            if match:
                metrics["mean_ttft_ms"] = float(match.group(1))
                found_any = True

            # Pattern: "Median TTFT: 118ms"
            match = re.search(r'Median\s+TTFT:\s*([\d.]+)\s*ms', content, re.IGNORECASE)
            if match:
                metrics["median_ttft_ms"] = float(match.group(1))
                found_any = True

            # Pattern: "Mean TPOT: 15ms" or "Mean TPOT: 15.2 ms"
            match = re.search(r'Mean\s+TPOT:\s*([\d.]+)\s*ms', content, re.IGNORECASE)
            if match:
                metrics["mean_tpot_ms"] = float(match.group(1))
                found_any = True

            # Pattern: "Median TPOT: 14ms"
            match = re.search(r'Median\s+TPOT:\s*([\d.]+)\s*ms', content, re.IGNORECASE)
            if match:
                metrics["median_tpot_ms"] = float(match.group(1))
                found_any = True

            # Pattern: "Throughput: 25 conversations/sec" or "Throughput: 25.3 conversations/sec"
            match = re.search(r'Throughput:\s*([\d.]+)\s+conversations?[/\s]*sec', content, re.IGNORECASE)
            if match:
                metrics["throughput_conversations_per_sec"] = float(match.group(1))
                found_any = True

            # Pattern: "Total tokens: 12450"
            match = re.search(r'Total\s+tokens:\s*(\d+)', content, re.IGNORECASE)
            if match:
                metrics["total_tokens"] = int(match.group(1))
                found_any = True

            # Pattern: "Average tokens per conversation: 245.5"
            match = re.search(r'Average\s+tokens\s+per\s+conversation:\s*([\d.]+)', content, re.IGNORECASE)
            if match:
                metrics["avg_tokens_per_conversation"] = float(match.group(1))
                found_any = True

            # Pattern: "Request rate: 10.5 req/s"
            match = re.search(r'Request\s+rate:\s*([\d.]+)\s+req', content, re.IGNORECASE)
            if match:
                metrics["request_rate"] = float(match.group(1))
                found_any = True

            result["metrics"] = metrics

            if found_any:
                # Check if we got all key metrics
                key_metrics = ["successful_conversations", "mean_ttft_ms", "mean_tpot_ms"]
                if all(m in metrics for m in key_metrics):
                    result["parsing_status"] = "success"
                else:
                    result["parsing_status"] = "partial"
                    result["parsing_warnings"].append(
                        f"Some key metrics missing. Found: {list(metrics.keys())}"
                    )
            else:
                result["parsing_status"] = "failed"
                result["parsing_errors"].append("No recognized metrics found in output")

        except Exception as e:
            result["parsing_errors"].append(f"Unexpected error: {str(e)}")

        return result

    def run(self, config: Dict[str, Any], run_dir: Path,
            params: Dict[str, Any], namespace: str) -> Dict[str, Any]:
        """Run vLLM multi-turn chat benchmark."""

        # Get configuration
        image = config.get('image', 'vllm/vllm-openai:latest')
        workload_file = config.get('workload_file', 'agent_multi_turn.json')

        # Get benchmark_args - handle both patterns
        benchmark_args_config = config.get('benchmark_args', {})
        if isinstance(benchmark_args_config, dict) and benchmark_args_config.get('type') in ['combinations', 'pairwise']:
            # New pattern: type: combinations or pairwise - extract fixed args (non-sweepable)
            benchmark_args = {}
            for key, value in benchmark_args_config.get('args', {}).items():
                # Skip sweepable parameters (they're in _load_params)
                if not (isinstance(value, dict) and 'values' in value):
                    benchmark_args[key] = value
        else:
            # Old pattern: direct dict or sweep_args pattern
            benchmark_args = benchmark_args_config.copy() if benchmark_args_config else {}

        # Merge load generation sweep parameters (if any)
        if '_load_params' in params:
            benchmark_args.update(params['_load_params'])

        # Resolve workload file path
        workload_path = Path(f"load-generators/multi-turn-benchmark/workloads/{workload_file}")
        if not workload_path.exists():
            raise FileNotFoundError(f"Workload file not found: {workload_path}")

        # Get model and construct service URL
        model = params.get('model')
        service_url = f"http://llm-d-inference-gateway-istio.{namespace}.svc.cluster.local:80"

        # Build script arguments (for run-benchmark.sh)
        # Note: results-dir is no longer passed - the hostPath is configured in Kustomize
        script_args = [
            "--image", image,
            "--namespace", namespace,
            "--workload-file", str(workload_path.absolute()),
            "--output-dir", str(run_dir.absolute())
        ]

        # Build benchmark arguments (passed to container after --)
        benchmark_cmd_args = self.build_args(
            benchmark_args,
            model,
            {'url': service_url}
        )

        # Add input-file pointing to mounted path
        benchmark_cmd_args.extend([
            "--input-file", f"/workload/{workload_file}"
        ])

        # Build full command
        cmd = ["./load-generators/multi-turn-benchmark/run-benchmark.sh"]
        cmd.extend(script_args)
        cmd.append("--")  # Separator
        cmd.extend(benchmark_cmd_args)

        print(f"  Running benchmark in pod (image: {image})...")

        # Execute
        result = subprocess.run(cmd, capture_output=True, text=True)

        if result.returncode != 0:
            print(f"  Warning: Benchmark exited with code {result.returncode}")

        # Save runner output
        with open(str(run_dir / "benchmark_runner_output.txt"), "w") as f:
            f.write(result.stdout)
            f.write(result.stderr)

        # Parse metrics from output file
        output_path = run_dir / "benchmark_output.txt"
        parsed_result = self.parse_metrics(output_path)

        return {
            "tool": "multi-turn-benchmark",
            "output_file": str(output_path),
            "exit_code": result.returncode,
            "image": image,
            "workload_file": workload_file,
            **parsed_result  # Add metrics, parsing_status, parsing_errors/warnings
        }
